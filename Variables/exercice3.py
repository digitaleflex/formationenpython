"""
Effectuer des opérations
Calculez la somme de deux nombres stockés dans des variables et affichez le résultat.

"""
# Déclaration de deux variables numériques
nombre1 = 25
nombre2 = 50

# Calcul de la somme
somme = nombre1 + nombre2

# Affichage du résultat avec f-string

print(f"La somme de {nombre1} et {nombre2} est égale à {somme}")

# Affichage du resultat avec la methode de concatenation 

print ('La somme de ' + str(nombre1) + ' et ' + str(nombre2) + ' est égale à ' + str(somme))

